package com.ktb.dao;

public class classNo {
    private String classNo;

    public String getClassNo() {
        return classNo;
    }

    public void setClassNo(String classNo) {
        this.classNo = classNo;
    }

    @Override
    public String toString() {
        return "classNo{" +
                "classNo='" + classNo + '\'' +
                '}';
    }
}
